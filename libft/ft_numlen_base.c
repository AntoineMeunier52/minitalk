#include "libft.h"

int	ft_numlen_base(unsigned long n, int base)
{
	int	i;

	i = 0;
	if (n == 0)
	{
		i += 1;
		return (i);
	}
	while (n > 0)
	{
		i++;
		n /= base;
	}
	return (i);
}
