#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*ptr;

	if (*alst)
	{
		ptr = ft_lstlast(*alst);
		ptr->next = new;
	}
	else
		*alst = new;
}
